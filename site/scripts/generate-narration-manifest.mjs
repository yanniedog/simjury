import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const docketDir = join(here, '..', 'app', 'docket')
const output = join(here, '..', 'src', 'narration-manifest.generated.js')
const stableVoices = Object.freeze({
  pc: 'thalia',
  dc: 'arcas',
  judge: 'athena',
  narrator: 'orion',
  w1: 'helena',
  w2: 'apollo',
  w3: 'andromeda',
  w4: 'aries',
  w5: 'aurora',
  w6: 'cordelia',
  w7: 'hyperion',
  w8: 'iris',
  'J-01': 'amalthea',
  'J-02': 'asteria',
  'J-03': 'atlas',
  'J-04': 'callista',
  'J-05': 'cora',
  'J-06': 'delia',
  'J-07': 'draco',
  'J-08': 'electra',
  'J-09': 'harmonia',
  'J-10': 'hera',
  'J-11': 'hermes',
})
const fallbackVoices = ['janus', 'juno', 'jupiter', 'luna', 'mars', 'minerva', 'neptune', 'odysseus']

function hash(value) {
  let h = 0x811c9dc5
  for (let i = 0; i < value.length; i++) h = Math.imul(h ^ value.charCodeAt(i), 0x01000193)
  return h >>> 0
}

export function narrationIdFor(text, key) {
  const slug = key.toLowerCase().replace(/[^a-z0-9-]/g, '-')
  return `${slug}-${hash(`${key}\0${text}`).toString(16).padStart(8, '0')}`
}

export function deepgramSpeakerFor(key) {
  return stableVoices[key] ?? fallbackVoices[hash(key) % fallbackVoices.length]
}

function linesFor(c) {
  const lines = c.hook ? [{ speaker: 'narrator', text: c.hook }] : []
  for (const phase of ['opening', 'closing']) {
    for (const side of ['prosecution', 'defence']) {
      const statement = c.statements?.[phase]?.[side]
      if (statement?.text && statement?.speaker) lines.push(statement)
    }
  }
  for (const beat of c.beats ?? []) {
    lines.push(...(beat.turns ?? [{ speaker: beat.speaker, text: beat.text }]))
  }
  for (const juror of c.jury?.jurors ?? []) {
    for (const texts of Object.values(juror.lines ?? {})) {
      if (!Array.isArray(texts)) continue
      for (const text of texts) lines.push({ speaker: juror.id, text })
    }
  }
  return lines
}

export function buildManifest(cases) {
  const entries = {}
  for (const c of cases) {
    for (const { speaker: key, text } of linesFor(c)) {
      const id = narrationIdFor(text, key)
      const entry = { text, speaker: deepgramSpeakerFor(key) }
      const prior = entries[id]
      if (prior && (prior.text !== text || prior.speaker !== entry.speaker)) {
        throw new Error(`narration id collision: ${id}`)
      }
      entries[id] = entry
    }
  }
  return Object.fromEntries(Object.entries(entries).sort(([a], [b]) => a.localeCompare(b)))
}

function generate() {
  const files = readdirSync(docketDir).filter((file) => /^dd-\d{4}\.json$/.test(file)).sort()
  const cases = files.map((file) => JSON.parse(readFileSync(join(docketDir, file), 'utf8')))
  const manifest = buildManifest(cases)
  mkdirSync(dirname(output), { recursive: true })
  writeFileSync(output, `// Generated by scripts/generate-narration-manifest.mjs. Do not edit.\nexport const narrationManifest = Object.freeze(${JSON.stringify(manifest)})\n`)
  console.log(`Generated ${Object.keys(manifest).length} whitelisted narration clips from ${files.length} dockets.`)
}

if (process.argv[1] && resolve(process.argv[1]) === resolve(fileURLToPath(import.meta.url))) generate()
